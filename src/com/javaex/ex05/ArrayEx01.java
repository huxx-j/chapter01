package com.javaex.ex05;

public class ArrayEx01 {
    public static void main(String[] args) {
        char[] c = new char[3];

        c[0] = 'B';
        c[1] = 'I';
        c[2] = 'T';

        System.out.println("c[0] : " + c[0]);
        System.out.println("c[1] : " + c[1]);
        System.out.println("c[2] : " + c[2]);
    }
}
